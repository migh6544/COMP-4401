# helper functions

def sqmToSqft(sqm):
    '''
    Converts square meters to square feet
    Input: a value (int or float) for square meters
    Returns:  a value (int of float) for square feet
    '''
    return sqm * 10.764

def eurosToDollars(euros):
    '''
    Converts Euros to Dollars
    Input: a value (int or float) for Euros
    Returns: a value (int or float) for Dollars
    '''
    return euros * 1.06